function rpGetLang(){
  return localStorage.getItem('rp_lang') || 'fr';
}

function rpSetLang(lang){
  localStorage.setItem('rp_lang', lang);
}

function rpT(key){
  const lang = rpGetLang();
  return (RP_TRANSLATIONS[lang] && RP_TRANSLATIONS[lang][key])
    || (RP_TRANSLATIONS.fr && RP_TRANSLATIONS.fr[key])
    || '';
}

function rpTf(key, vars){
  let text = rpT(key);
  if(!vars) return text;
  Object.keys(vars).forEach(k => {
    const re = new RegExp(`{{\\s*${k}\\s*}}`, 'g');
    text = text.replace(re, String(vars[k]));
  });
  return text;
}

function rpApplyLang(){
  const lang = rpGetLang();
  const isRtl = lang === 'ar';
  document.documentElement.lang = lang;
  if(document.body){
    document.body.classList.toggle('rtl', isRtl);
  }

  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = rpT(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    el.innerHTML = rpT(el.dataset.i18nHtml);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.setAttribute('placeholder', rpT(el.dataset.i18nPlaceholder));
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.setAttribute('title', rpT(el.dataset.i18nTitle));
  });
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    el.setAttribute('aria-label', rpT(el.dataset.i18nAria));
  });

  const title = document.querySelector('title[data-i18n]');
  if(title){
    title.textContent = rpT(title.dataset.i18n);
  }

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

document.addEventListener('DOMContentLoaded', () => {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      rpSetLang(btn.dataset.lang);
      rpApplyLang();
      document.dispatchEvent(new CustomEvent('rp:lang'));
    });
  });
  rpApplyLang();
});
